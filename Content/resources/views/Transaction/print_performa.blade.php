   <?php
function numToWords($number)
{
    $words = array(
        '0' => 'Zero', '1' => 'One', '2' => 'Two',
        '3' => 'Three', '4' => 'Four', '5' => 'Five',
        '6' => 'Six', '7' => 'Seven', '8' => 'Eight',
        '9' => 'Nine', '10' => 'Ten', '11' => 'Eleven',
        '12' => 'Twelve', '13' => 'Thirteen', '14' => 'Fourteen',
        '15' => 'Fifteen', '16' => 'Sixteen', '17' => 'Seventeen',
        '18' => 'Eighteen', '19' => 'Nineteen', '20' => 'Twenty',
        '30' => 'Thirty', '40' => 'Forty', '50' => 'Fifty',
        '60' => 'Sixty', '70' => 'Seventy', '80' => 'Eighty',
        '90' => 'Ninety'
    );

    $digits = array('', 'Thousand', 'Million', 'Billion');

    if ($number == 0) return 'Zero';

    $number = (string) $number;
    $number = ltrim($number, '0');
    $length = strlen($number);
    $i = 0;
    $words_string = '';

    while ($length > 0) {
        $n = intval(substr($number, max(0, $length - 3), min(3, $length)));
        if ($n != 0) {
            $str = '';
            $hundred = intval($n / 100);
            $n = $n % 100;
            if ($hundred) {
                $str .= $words[$hundred] . ' Hundred ';
            }
            if ($n) {
                if ($n < 20) {
                    $str .= $words[$n] . ' ';
                } else {
                    $str .= $words[10 * intval($n / 10)] . ' ';
                    if (($n % 10) > 0) {
                        $str .= $words[$n % 10] . ' ';
                    }
                }
            }
            $words_string = $str . $digits[$i] . ' ' . $words_string;
        }
        $i++;
        $length -= 3;
    }

    return trim($words_string);
}
